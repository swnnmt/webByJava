    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
       request.getRequestDispatcher("Front/account_user.jsp").forward(request, response);
    } 

    /** 
     * Handles the HTTP <code>POST</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        String username= request.getParameter("username");
        String id_account_raw= request.getParameter("id_account");
        String email = request.getParameter("email");
        String password = request.getParameter("password");
        cartegoryDAO c=new cartegoryDAO();
        HttpSession session= request.getSession();
        int id_account;
        try {
            id_account= Integer.parseInt(id_account_raw);
            c.updateAccountById(id_account, username, email, password);
            Account account_login= c.getAccountByUsername(username, password);
             session.setAttribute("account_login", account_login);
            response.sendRedirect("home_front");
        } catch (Exception e) {
        }
    }

